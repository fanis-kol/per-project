class Student {
    constructor(student_id, name, last_name, date_of_birth, tuition_fees) {
        this.student_id = student_id;
        this.name = name;
        this.last_name = last_name;
        this.date_of_birth = date_of_birth;
        this.tuition_fees = tuition_fees;
        
    }
}
module.exports = Student;